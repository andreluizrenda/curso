x = 10
y = 5


print("Resultado da soma de",x,"+",y, "eh:",x+y)

print("Resultado da subtracao de",x,"-",y, "eh:",x-y)
print("Resultado da multplicacao de",x,"-",y, "eh:",x*y)
print("Resultado da divisao de",x,"-",y, "eh:",x/y)
print("Resultado da divisao inteira de",x,"-",y, "eh:",x//y)
print("Resultado do resto da divisao de",x,"-",y, "eh:",x%y)


